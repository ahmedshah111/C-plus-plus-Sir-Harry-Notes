#include<iostream>
using namespace std;

int main()
{
    int num1, num2;
    /* cout<<  (<<) is called Insertion Operator*/
    /* cin<<  (>>) is called Extraction Operator*/
    cout<<"Enter the value of num1:\n";
    cin>>num1;

    cout<<"Enter the value of num2:\n";
    cin>>num2;

    cout<<"Sum is "<<num1 + num2;
    return 0;
}
